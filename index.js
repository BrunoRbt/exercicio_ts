// Função de multiplicação utilizando arrow function
var multiplicar = function (a, b) { return a * b; };
// Função de saudação utilizando arrow function
var saudacao = function (nome) { return "Ol\u00E1 ".concat(nome); };
// Exemplos de uso
var resultadoMultiplicacao = multiplicar(5, 10);
console.log("Resultado da multiplica\u00E7\u00E3o: ".concat(resultadoMultiplicacao)); // Saída: 50
var mensagemSaudacao = saudacao("Maria");
console.log(mensagemSaudacao); // Saída: Olá Maria
